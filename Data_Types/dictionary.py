# Dictionary is a collection of key plus value
# Syntax of Dictionary 
'''
dictionary = {}

'''
# Initialize dict

my_dict : dict = {}
print(my_dict)
# How to add element

my_dict["Pizza"] = 120

# How to remove element

del my_dict["Pizza"]
print(my_dict)

# How to access value or kay

item = my_dict["Pizza"]

print(item)

price = my_dict["Pizza"]

print(price)

# How to update dict

my_dict["Pizza"] = 150

price = my_dict["Pizza"]

print(price)        

# How to clear dict

my_dict.clear()
print(my_dict)

# How to find lenght of dict

my_dict.__len__()

print(my_dict)